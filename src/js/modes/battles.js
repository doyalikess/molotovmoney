// user can change amount case
function changeCasesAmount() {
	let amount = parseInt(this.parentElement.children[1].textContent);

	if (
		this.classList.contains("battles__item-btn-minus") &&
		parseInt(this.parentElement.children[1].textContent) > 1
	) {
		amount = amount - 1;
		this.parentElement.children[1].textContent = amount;

		const idOfItem = this.parentElement.parentElement.id;
		amountOfCases[`${idOfItem}`].amount = amount;
	} else if (this.classList.contains("battles__item-btn-plus")) {
		amount = amount + 1;
		this.parentElement.children[1].textContent = amount;

		const idOfItem = this.parentElement.parentElement.id;
		amountOfCases[`${idOfItem}`].amount = amount;
	}

	countTotalCost();
}

